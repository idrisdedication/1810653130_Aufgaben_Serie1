public class Main
{
    public static void main(String[] args) {    // public weil von außerhalb der Klasse auf sie zugegriffen wird
                                                // static weil sie aufgerufen wird ohne dass vorher ein Objekt einer Klasse gebildet wurde
                                                // void weil sie keinen Rückgabewert besitzt
                                                // der Parameter String[] args, der auch String args[] geschrieben werden darf, ist ein Array, das die vom Aufruf entgegen genommenen Kommandozeilenparameter enthält
        System.out.println("Hello World"); // bei System.out.println wird nach der Ausgabe ein Absatz eingefügt
                                                             // Hello World wird auf der Konsole ausgegeben
        System.out.println("Abdullatif Atalay "); // Abdullatif Atalay wird auf auf der Konsole ausgegeben
    }
}
